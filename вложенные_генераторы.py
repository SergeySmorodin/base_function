                                 # Необходимо преобразовать двумерный список lst_in в одномерный 

# так, чтобы значения элементов шли в обратном порядке.
# Sample Input:     # 1 2 3 4
                    # 5 6 7 8
                    # 9 8 7 6
                    # 5 4 3 2   
# Sample Output: 2 3 4 5 6 7 8 9 8 7 6 5 4 3 2 1
lst_in = [[1, 2, 3, 4], [5, 6, 7, 8], [9, 8, 7, 6], [5, 4, 3, 2]]

lst = [x 
       for row in lst_in 
       for x in row][::-1]

print(*lst)



                                # Сформировать двумерный список lst размером N x N (квадратную таблицу чисел)

# Sample Input: 1 2 3 4 5 6 7 8 9
# Sample Output: [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
lst_in = [1, 2, 3, 4, 5, 6, 7, 8, 9]

n = int((len(lst_in))**0.5) # вычислим корень из длинны введенного списка для формирования длины вложенных списков (3)
src_iter = iter(lst_in) # назначаем итератор списка lst_in

result = [[next(src_iter) for j in range(n)] for i in range(n)] # с помощью итератора перебираем по 3 значения из списка lst_in

print(result)


