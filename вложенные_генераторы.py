                                                    # Транспонирование матрицы A 

# Сначала значение i = 0, а переменная j[i] пробегает первые значения строк матрицы A. 
# В результате формируется первая строка транспонированной матрицы. Далее, i увеличивается на 1 
# и j[i] пробегает уже вторые элементы строк матрицы A. 
# Получаем вторую строку транспонированной матрицы. 
# И так делаем для всех столбцов. 
# На выходе формируется транспонированная матрица.


lst_in = [[1, 2, 3], 
          [5, 6, 7], 
          [9, 8, 7], 
          [5, 4, 3]]


A = [[j[i] for j in lst_in] for i in range(len(lst_in[0]))]
for row in A:
    print(*row)
